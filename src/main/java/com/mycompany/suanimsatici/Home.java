    private void btnArtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArtiActionPerformed
       int amount=cmbAmount.getSelectedIndex();
        switch (amount) {
            case 1:
                Hedef=1500;
              miktarArttırma();             
                break;
            case 2:
                Hedef=2400;
               miktarArttırma();
                break;
            case 3:
                Hedef=3000;
                miktarArttırma();
                break;
            default: 
                 JOptionPane.showMessageDialog(null,"Miktar giriniz!");
                break;
        }
     
        
 
    }//GEN-LAST:event_btnArtiActionPerformed

