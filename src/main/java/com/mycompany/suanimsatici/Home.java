    private void miktarArttırma(){
            if(Hedef!=icilenMiktar){
                    if(icilenMiktar==0){                   
                    icilenMiktar+=150; 
                    lblIcilenMiktar.setText(String.valueOf(icilenMiktar));
                    KalanMiktar=Hedef-icilenMiktar;
                    lblKalanMiktar.setText(String.valueOf(KalanMiktar));
                    sql.Ekle();
                    }
                    else if(icilenMiktar>0){                
                     icilenMiktar+=150; 
                     lblIcilenMiktar.setText(String.valueOf(icilenMiktar));
                     KalanMiktar=Hedef-icilenMiktar;
                     lblKalanMiktar.setText(String.valueOf(KalanMiktar));
                    sql.Guncelle(); 
                    }
                }
    }
    private void miktarAzaltma(){
        if(icilenMiktar==150){
             icilenMiktar-=150;
             sql.Sil();
             lblIcilenMiktar.setText(String.valueOf(icilenMiktar));                        
             KalanMiktar+=150;
             lblKalanMiktar.setText(String.valueOf(KalanMiktar));
        }
        else{
             icilenMiktar-=150;
             lblIcilenMiktar.setText(String.valueOf(icilenMiktar));                        
             KalanMiktar+=150;
             lblKalanMiktar.setText(String.valueOf(KalanMiktar));
             sql.Guncelle();
        }
    }
